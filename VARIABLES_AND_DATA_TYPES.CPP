#include <iostream>
using namespace std;

int main() {
    int age = 25;               // Integer variable
    double price = 19.99;       // Double (floating-point) variable
    char grade = 'A';           // Character variable
    string name = "John";       // String variable

    cout << "Name: " << name << ", Age: " << age << ", Grade: " << grade << endl;
    cout << "Price: " << price << endl;

    return 0;
}
