#include <iostream>
using namespace std;

int main() {
    // Example using a for loop
    for (int i = 1; i <= 5; i++) {
        cout << "Iteration " << i << endl;
    }

    // Example using a while loop
    int j = 1;
    while (j <= 5) {
        cout << "Iteration " << j << endl;
        j++;
    }

    return 0;
}
