#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 5;
    int sum = a + b;
    int difference = a - b;
    int product = a * b;
    int quotient = a / b;

    cout << "Sum: " << sum << ", Difference: " << difference << endl;
    cout << "Product: " << product << ", Quotient: " << quotient << endl;

    return 0;
}
